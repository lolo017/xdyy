public class Details {
	private String item_code;	//商品代码
	private String item_name;	//商品名称
	private String sku_name;	//规格名称
	private String is_gift;	//赠品标识:1赠品
	private BigDecimal qty;	//数量
	private BigDecimal amount_after; //让利后金额
	private BigDecimal post_fee;	//物流费用
	
	public String getItem_code() {
		return item_code;
	}
	public void setItem_code(String item_code) {
		this.item_code = item_code;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getSku_name() {
		return sku_name;
	}
	public void setSku_name(String sku_name) {
		this.sku_name = sku_name;
	}
	public BigDecimal getQty() {
		return qty;
	}
	public void setQty(BigDecimal qty) {
		this.qty = qty;
	}
	public BigDecimal getAmount_after() {
		return amount_after;
	}
	public void setAmount_after(BigDecimal amount_after) {
		this.amount_after = amount_after;
	}
	public BigDecimal getPost_fee() {
		return post_fee;
	}
	public void setPost_fee(BigDecimal post_fee) {
		this.post_fee = post_fee;
	}
	public String getIs_gift() {
		return is_gift;
	}
	public void setIs_gift(String is_gift) {
		this.is_gift = is_gift;
	}
	
	
}
